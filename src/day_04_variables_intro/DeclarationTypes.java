package day_04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args) {
        //declaration  + assignments in single statement:
        int bananas = 34;
        System.out.println(bananas);

        int students = 400;
        System.out.println(students);

        students = 401;
        System.out.println(students);

        int peopleInCity, birdsOnTree, fishLake;
        peopleInCity = 1000;
        birdsOnTree = 21;
        fishLake = 100;

        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishLake);

        int studentsInNy = 58, studentsInVA = 200;
        System.out.println(studentsInNy);
        System.out.println(studentsInVA);

    }
}
