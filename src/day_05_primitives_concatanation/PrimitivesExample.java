package day_05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args) {
        byte byteValue = 100;
        short shortValue = 32000;
        int intValue = 21235256;
        long longValue = 5221256545869851264L; //we have to pu L at the end..
        float floatValue = 2345.25F; // we have to put F or f for float.
        double doubleValue = 2541.6545;
        char charValue = 'A';
        boolean booleanValue = true;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
    }
}
