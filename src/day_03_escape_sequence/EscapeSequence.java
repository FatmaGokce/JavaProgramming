package day_03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("I like \"java\" programming");
        System.out.println("Lets buy \"coffee\" and \"tea\"");
        System.out.println("\\");
        System.out.println("Java\nintellij");
        System.out.println("\"");
        System.out.println("\'");
        System.out.println("\tjava\tapi\tselenium");
    }
}